/**
 * Filter match mode type.
 *
 * @author Roman Batygin
 */
public enum MatchMode {

    /**
     * Equals match mode
     */
    EQUALS,

    /**
     * Less or equals than match mode
     */
    LTE,

    /**
     * Greater or equals than match mode
     */
    GTE
}
